							<div class="item-faq">
								<h3 class="accordion not_bd">1. Can blockchains be used as a platform to build all types of games? </h3>
								<div class="panel">
									<div class="txt-answer">No. Blockchain-based games should be creative. Heavily and highly interactive games are not fit with blockchains at the current development stage.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion">2. As a developer, what are the advantages of becoming a blockchain gaming developer compared to traditional gaming developers?</h3>
								<div class="panel">
									<div class="txt-answer">Blockchain gaming market is exploring and not many game developers have blockchain skills. Having blockchain and economics mindset would enable new generation of developers to be highly competitive with others.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion">3. Why does blockchains allow game players to easily trade in-game virtual items?</h3>
								<div class="panel">
									<div class="txt-answer">In-game virtual items are tokenized and can be easily traded in decentralized exchanges.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion">4. I have an already running game on a centralized server, can I move my entire game onto a blockchain to minimize my server cost?</h3>
								<div class="panel">
									<div class="txt-answer">No. If you game is highly interactive, it’d be best to analyze its logic and economics mechanism to appropriate parts (ownership, payment…) onto blockchains, while keeping others on a centralized server.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion not_bd">5. What is the economics mechanism in blockchain-based games?</h3>
								<div class="panel">
									<div class="txt-answer">The economics mechanism allows game developers to incentivize and penalize gamers, using a tokenomics model automatically built in smart contracts.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion">7. Are blockchains too slow to be able to run highly interactive games?</h3>
								<div class="panel">
									<div class="txt-answer">At this early stage, yes. However, more blockchain innovations such as LOOM and TomoChain are built to enable a millions TPS system for games.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion">8. Does building games on blockchains mean putting all game data on blockchains?</h3>
								<div class="panel">
									<div class="txt-answer">No. The best game should be a combination of centralized and blockchain games, that would use blockchain only when needed in order to profit from both centralized and decentralized worlds.</div>
								</div>
							</div>
							<div class="item-faq">
								<h3 class="accordion">9. I have heard lots about blockchain scalability, that blockchains like Ethereum cannot process more than 15 tps, if so, how is that possible to expect users to have good user experience for a game on blockchains?</h3>
								<div class="panel">
									<div class="txt-answer">Scalability in blockchain is still a problem for both users and developers at this early stage. However, more innovations have been addressing this issue, e.g. TomoChain, LOOM, ENJIN, XAYA.</div>
								</div>
